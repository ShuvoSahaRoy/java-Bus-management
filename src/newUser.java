    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      if(validateFields()){
        try {
            stmt = conn.createStatement();
            int userId = Integer.parseInt(id.getText());
            String firstName = t1.getText();
            String lastName = t2.getText();
            String userName = t3.getText();
            String pass = t4.getText();
            String email = t5.getText();
            String web = t6.getText();
            
            String sql = "Insert into user values('" + userId + "','" + firstName + "','" + lastName + "','" + userName + "','" + pass + "','" + email + "','" + web + "')";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data is successfully inserted.");
            dispose();
            userLogin ob = new userLogin();
            ob.setVisible(true);
            
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Id is already taken");
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

